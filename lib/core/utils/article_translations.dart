  String getLocalizedBody(String contentLang) {
    if (contentLang == 'Hindi') {
      return _hindiBodies[id] ?? body;
    } else if (contentLang == 'Both') {
      return '${_hindiBodies[id] ?? body}\n\n$body';
    }
    return body;
  }
